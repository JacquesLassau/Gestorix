CREATE PROCEDURE [dbo].[SP_ListarPedidos]
	
AS
BEGIN
	Select IdPEDIDO, 
			DATaHORaFINALIZADO, 
			DATaHORaEXCLUIDO, 
			DESCRICAO, 
			TOTAlCUSTO, 
			TOTAlPEDIDO, 
			TOTAlLUCRO, 
			SITUACAO, 
			COUNT(*) over() as TOTAL_PEDIDOS_ORCADOS 
	From 
		Pedido 
    Order By IdPEDIDO Desc
END
GO