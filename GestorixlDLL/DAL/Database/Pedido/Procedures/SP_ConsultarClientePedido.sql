CREATE PROCEDURE [dbo].[SP_ConsultarClientePedido] 
 @IdPedido int
AS  
BEGIN
	SELECT 	
		IdPEDIDO,
		NOME,
		DOCUMENTO,			
		ENDERECO,		
		BAIRRO,
		CIDADE,
		UF,
		CEP
	FROM 
		ClientePedido 
	WHERE
		IdPEDIDO = @IdPedido
END